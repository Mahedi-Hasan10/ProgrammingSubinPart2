#include<stdio.h>
int main()
{
    char ch1 = 'A', ch2 ='B';
    int n1 = 50, n2 = 324;
    printf("The value of ch1 is %c\n",ch1);
    printf("The address of ch1 is %d\n",&ch1);

    printf("The value of ch2 is %c\n",ch2);
    printf("The address of ch2 is %d\n",&ch2);

    printf("The value of n1 is %c\n",n1);
    printf("The address of n1 is %d\n",&n1);

    printf("The value of n2 is %c\n",n2);
    printf("The address of n2 is %d\n",&n2);

    return 0;
}